def sift(rawGolem):
    """
    Takes a raw golem and removes everything that isn't a valid resource
    character; returns a shaped golem string.
    """

    shapedGolem = []

    for piece in rawGolem:
        if piece in ['~', '@', '#', '^', '&', '*', '[', ']']:
            shapedGolem.append(piece)

    return "".join(shapedGolem)

